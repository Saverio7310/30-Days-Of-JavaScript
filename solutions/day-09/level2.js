function categorizeCountries(countries, patternValue, pattern) {
    let func
    switch (pattern) {
        case 'starts with':
            func = String.prototype.startsWith
            break;
        case 'contains':
            func = String.prototype.includes
            break
        case 'ends with':
            func = String.prototype.endsWith
    }
    return countries.filter((country) => func.call(country, patternValue))
}

