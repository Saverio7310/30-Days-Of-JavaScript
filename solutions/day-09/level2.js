function letterMap(countries, arr){
    //'a' char code = 97
    for (let index = 97; index < 123; index++) {
        arr[index-97] = {
            letter: String.fromCharCode(index),
            times: 0
        }
    }
    /**
     * 
     * @param {string} country 
     */
    function getLetters(country){
        const letter = country.charAt(0).toLowerCase()
        arr[letter.charCodeAt(0)-97].times += 1
    }
    countries.forEach(getLetters)
}
